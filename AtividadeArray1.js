//includes() verifica se um array contém um determinado 
//elemento e retorna true ou false caso verdadeiro ou falso


let numeros = [1, 2, 3, 4, 5];
let verificar = numeros.includes(3);
// "verificar" é igual a true
console.log (verificar)

let frutas = ["maçã", "banana", "laranja"];
let verifica = frutas.includes("pera");
// "verifica" é igual a false
console.log (verifica)


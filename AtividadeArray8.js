//lastIndexOf() retorna o último índice em que um elemento específico 
//pode ser encontrado no array, ou -1 se o elemento não estiver 
//presente.

let numeros = [10, 20, 30, 40, 30, 50];
let indicetrinta = numeros.lastIndexOf(30);
// indicetrinta é igual a 4
console.log(indicetrinta)

let letras = ["a", "b", "c", "a", "d"];
let indiceA = letras.lastIndexOf("a");
// indiceA é igual a 3
console.log(indiceA)

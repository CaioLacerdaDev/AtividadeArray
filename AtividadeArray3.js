//sort() organiza os elementos de um array em ordem 
//alfabética (quando aplicado a strings) ou em ordem 
//crescente (quando aplicado a números) 

let frutas = ['laranja', 'maçã', 'banana'];
frutas.sort();
//frutas contém ['banana', 'laranja', 'maçã']
console.log (frutas) 

let numeros = [3, 1, 5, 2, 4];
numeros.sort();
//numeros contém [1, 2, 3, 4, 5]
console.log (numeros)
